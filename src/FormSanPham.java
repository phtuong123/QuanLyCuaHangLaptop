    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       // 1. Lấy dữ liệu từ giao diện
        String tenSP = txtTenSP.getText().trim();
        String tenLoai = cboLoaiSP.getSelectedItem().toString();
        String giaBanStr = txtGiaBan.getText().trim();
        String soLuongStr = txtSoLuong.getText().trim();
        // String moTa = txtMoTa.getText().trim(); // Nếu có TextArea mô tả

        // 2. Kiểm tra dữ liệu đầu vào (Validation)
        if (tenSP.isEmpty() || giaBanStr.isEmpty() || soLuongStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin (Tên, Giá, Số lượng)!");
            return;
        }
        if (cboLoaiSP.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Loại sản phẩm!");
            return;
        }

        try {
            // Chuyển đổi kiểu dữ liệu và kiểm tra số
            double giaBan = Double.parseDouble(giaBanStr);
            int soLuong = Integer.parseInt(soLuongStr);

            if (giaBan < 0 || soLuong < 0) {
                JOptionPane.showMessageDialog(this, "Giá bán và Số lượng phải là số dương!");
                return;
            }

            Connection conn = DBConnect.getConnection();

            // --- BƯỚC QUAN TRỌNG: LẤY MÃ LOẠI TỪ TÊN LOẠI ---
            // Vì trong bảng SanPham lưu MaLoai (số), nhưng trên combobox lại hiện TenLoai (chữ)
            String sqlGetMaLoai = "SELECT MaLoai FROM LoaiSP WHERE TenLoai = ?";
            PreparedStatement psMaLoai = conn.prepareStatement(sqlGetMaLoai);
            psMaLoai.setString(1, tenLoai);
            ResultSet rsMaLoai = psMaLoai.executeQuery();
            
            int maLoai = -1;
            if (rsMaLoai.next()) {
                maLoai = rsMaLoai.getInt("MaLoai");
            } else {
                 // Trường hợp này hiếm khi xảy ra nếu combobox load đúng
                 JOptionPane.showMessageDialog(this, "Lỗi: Không tìm thấy mã loại sản phẩm!");
                 conn.close();
                 return;
            }
            // ------------------------------------------------

            // 3. Thực hiện câu lệnh INSERT
            String sqlInsert = "INSERT INTO SanPham (TenSP, MaLoai, GiaBan, SoLuongTon) VALUES (?, ?, ?, ?)";
            // Nếu có mô tả: "INSERT INTO SanPham (TenSP, MaLoai, GiaBan, SoLuongTon, MoTa) VALUES (?, ?, ?, ?, ?)"
            
            PreparedStatement ps = conn.prepareStatement(sqlInsert);
            ps.setString(1, tenSP);
            ps.setInt(2, maLoai);
            ps.setDouble(3, giaBan);
            ps.setInt(4, soLuong);
            // Nếu có mô tả: ps.setString(5, moTa);

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
                // Reset lại giao diện và tải lại bảng
                btnLamMoiActionPerformed(evt); 
            }

            conn.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá bán và Số lượng phải là số hợp lệ!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi thêm sản phẩm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
      // Kiểm tra xem đã chọn sản phẩm nào chưa
        if (maSPDangChon == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần sửa trong bảng!");
            return;
        }

        // 1. Lấy dữ liệu từ giao diện (Tương tự nút Thêm)
        String tenSP = txtTenSP.getText().trim();
        String tenLoai = cboLoaiSP.getSelectedItem().toString();
        String giaBanStr = txtGiaBan.getText().trim();
        String soLuongStr = txtSoLuong.getText().trim();
        // String moTa = txtMoTa.getText().trim();

        // 2. Validation (Tương tự nút Thêm)
        if (tenSP.isEmpty() || giaBanStr.isEmpty() || soLuongStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }
        if (cboLoaiSP.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Loại sản phẩm!");
            return;
        }

        try {
            double giaBan = Double.parseDouble(giaBanStr);
            int soLuong = Integer.parseInt(soLuongStr);
            if (giaBan < 0 || soLuong < 0) {
                 JOptionPane.showMessageDialog(this, "Giá và Số lượng phải dương!"); return;
            }

            Connection conn = DBConnect.getConnection();

            // Lấy Mã Loại từ Tên Loại
            String sqlGetMaLoai = "SELECT MaLoai FROM LoaiSP WHERE TenLoai = ?";
            PreparedStatement psMaLoai = conn.prepareStatement(sqlGetMaLoai);
            psMaLoai.setString(1, tenLoai);
            ResultSet rsMaLoai = psMaLoai.executeQuery();
            int maLoai = -1;
            if (rsMaLoai.next()) maLoai = rsMaLoai.getInt("MaLoai");

            // 3. Thực hiện câu lệnh UPDATE
            String sqlUpdate = "UPDATE SanPham SET TenSP=?, MaLoai=?, GiaBan=?, SoLuongTon=? WHERE MaSP=?";
            // Nếu có mô tả: "UPDATE SanPham SET TenSP=?, MaLoai=?, GiaBan=?, SoLuongTon=?, MoTa=? WHERE MaSP=?"

            PreparedStatement ps = conn.prepareStatement(sqlUpdate);
            ps.setString(1, tenSP);
            ps.setInt(2, maLoai);
            ps.setDouble(3, giaBan);
            ps.setInt(4, soLuong);
            // Nếu có mô tả: ps.setString(5, moTa); ps.setInt(6, maSPDangChon);
            ps.setInt(5, maSPDangChon); // Nếu không có mô tả thì tham số này là số 5

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật sản phẩm thành công!");
                btnLamMoiActionPerformed(evt); // Reset
            }

            conn.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Lỗi định dạng số!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi sửa sản phẩm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

