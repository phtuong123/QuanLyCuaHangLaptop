    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
       int row = tblKhachHang.getSelectedRow();
        if (row >= 0) {
            maKHDangChon = Integer.parseInt(model.getValueAt(row, 0).toString());
            
            // Đổ dữ liệu lên các ô text
            txtMaKH.setText(model.getValueAt(row, 0).toString());
            txtHoTen.setText(model.getValueAt(row, 1).toString());
            txtSDT.setText(model.getValueAt(row, 2).toString());
            
            // Xử lý trường hợp Email hoặc Địa chỉ bị Null trong database
            Object emailObj = model.getValueAt(row, 3);
            txtEmail.setText(emailObj != null ? emailObj.toString() : "");
            
            Object diaChiObj = model.getValueAt(row, 4);
            txtDiaChi.setText(diaChiObj != null ? diaChiObj.toString() : "");

            // Mở khóa nút Sửa/Xóa
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked

