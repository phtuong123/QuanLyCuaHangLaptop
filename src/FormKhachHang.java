    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (!checkValidate()) return; // Kiểm tra đầu vào

        try {
            Connection conn = DBConnect.getConnection();
            String sql = "INSERT INTO KhachHang (HoTen, SDT, Email, DiaChi) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtHoTen.getText().trim());
            ps.setString(2, txtSDT.getText().trim());
            ps.setString(3, txtEmail.getText().trim());
            ps.setString(4, txtDiaChi.getText().trim());

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
                btnLamMoiActionPerformed(evt); // Reset form
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi thêm: " + e.getMessage());
        }
   
      
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (maKHDangChon == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần sửa!");
            return;
        }
        if (!checkValidate()) return;

        try {
            Connection conn = DBConnect.getConnection();
            String sql = "UPDATE KhachHang SET HoTen=?, SDT=?, Email=?, DiaChi=? WHERE MaKH=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtHoTen.getText().trim());
            ps.setString(2, txtSDT.getText().trim());
            ps.setString(3, txtEmail.getText().trim());
            ps.setString(4, txtDiaChi.getText().trim());
            ps.setInt(5, maKHDangChon);

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                btnLamMoiActionPerformed(evt);
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi sửa: " + e.getMessage());
        }
       
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
      if (maKHDangChon == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần xóa!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa khách hàng này?\n(Lưu ý: Chỉ xóa được nếu khách hàng chưa từng mua hàng)", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = DBConnect.getConnection();
                String sql = "DELETE FROM KhachHang WHERE MaKH=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, maKHDangChon);

                int rows = ps.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công!");
                    btnLamMoiActionPerformed(evt);
                }
                conn.close();
            } catch (java.sql.SQLIntegrityConstraintViolationException ie) {
                JOptionPane.showMessageDialog(this, "Không thể xóa! Khách hàng này đã có lịch sử mua hàng (tồn tại trong hóa đơn).");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi xóa: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

