    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
      // 1. Lấy dữ liệu từ giao diện
    String maNV = txtMaNV.getText(); // <--- MỚI
    String hoten = txtHoTen.getText();
    String chucvu = txtChucVu.getText();
    String ngaysinh = txtNgaySinh.getText(); 
    String luong = txtLuong.getText();
    String taikhoan = txtTaiKhoan.getText();
    String matkhau = txtMatKhau.getText();

    // 2. Kiểm tra nhập thiếu (Phải kiểm tra cả Mã NV)
    if(maNV.equals("") || hoten.equals("")){
        JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã NV và Họ tên!");
        return;
    }

    try {
        Connection conn = DBConnect.getConnection();
        
        // --- CÂU SQL MỚI: Có thêm cột MaNV ở đầu ---
        String sql = "INSERT INTO NhanVien (MaNV, HoTen, ChucVu, NgaySinh, Luong, TaiKhoan, MatKhau) VALUES (?,?,?,?,?,?,?)";
        
        PreparedStatement ps = conn.prepareStatement(sql);
        
        ps.setInt(1, Integer.parseInt(maNV)); // <--- Set tham số số 1 là Mã NV
        ps.setString(2, hoten);
        ps.setString(3, chucvu);
        ps.setString(4, ngaysinh);
        ps.setDouble(5, Double.parseDouble(luong));
        ps.setString(6, taikhoan);
        ps.setString(7, matkhau);
        
        ps.executeUpdate(); 
        
        JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
        loadData(); 
        conn.close();
    } catch (Exception e) {
        // Bắt lỗi trùng Mã NV (Ví dụ nhập số 1 hai lần)
        if(e.getMessage().contains("Duplicate entry")){
            JOptionPane.showMessageDialog(this, "Mã nhân viên " + maNV + " đã tồn tại! Vui lòng chọn số khác.");
        } else {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi thêm: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblNhanVien.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên để sửa!");
        return;
    }
    
    try {
        // Lấy Mã NV để biết đang sửa ai (Mã NV không được sửa)
        String maNV = tblNhanVien.getValueAt(row, 0).toString();
        
        Connection conn = DBConnect.getConnection();
        String sql = "UPDATE NhanVien SET HoTen=?, ChucVu=?, NgaySinh=?, Luong=?, TaiKhoan=?, MatKhau=? WHERE MaNV=?";
        PreparedStatement ps = conn.prepareStatement(sql);
        
        ps.setString(1, txtHoTen.getText());
        ps.setString(2, txtChucVu.getText());
        ps.setString(3, txtNgaySinh.getText());
        ps.setDouble(4, Double.parseDouble(txtLuong.getText()));
        ps.setString(5, txtTaiKhoan.getText());
        ps.setString(6, txtMatKhau.getText());
        ps.setString(7, maNV); // Điều kiện WHERE
        
        ps.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        loadData();
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi sửa: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // 1. Kiểm tra xem người dùng đã chọn dòng nào chưa
    int row = tblNhanVien.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa trên bảng!");
        return;
    }

    // 2. Hỏi xác nhận cho chắc ăn
    int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa nhân viên này?");
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // Lấy MaNV ở cột đầu tiên (cột 0) của dòng đang chọn
            String maNV = tblNhanVien.getValueAt(row, 0).toString();
            
            Connection conn = DBConnect.getConnection();
            String sql = "DELETE FROM NhanVien WHERE MaNV = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, maNV);
            
            ps.executeUpdate(); // Thực thi xóa
            
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadData(); // Tải lại bảng
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi xóa: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnXoaActionPerformed

