    private void loadData() {
        try {
            Connection conn = DBConnect.getConnection();
            
            // Câu lệnh này lấy dữ liệu từ 3 bảng: HoaDon, NhanVien, KhachHang
            // Để thay vì hiện số (1, 2) nó hiện Tên (Tường, Duy...)
            String sql = "SELECT H.MaHD, H.NgayLap, N.HoTen AS TenNV, K.HoTen AS TenKH, H.TongTien " +
                         "FROM HoaDon H " +
                         "JOIN NhanVien N ON H.MaNV = N.MaNV " +
                         "JOIN KhachHang K ON H.MaKH = K.MaKH";
            
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            // Tạo khung cho bảng
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Mã HĐ");
            model.addColumn("Ngày Lập");
            model.addColumn("Nhân Viên");
            model.addColumn("Khách Hàng");
            model.addColumn("Tổng Tiền");
            
            // Đổ dữ liệu vào
            while(rs.next()){
                Vector row = new Vector();
                row.add(rs.getInt("MaHD"));
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                row.add(sdf.format(rs.getTimestamp("NgayLap")));
                row.add(rs.getString("TenNV"));
                row.add(rs.getString("TenKH"));
                
                // Lấy tổng tiền (ép sang kiểu long cho mất số lẻ)
                row.add((long)rs.getDouble("TongTien"));
                
                model.addRow(row);
            }
            
            // Gán vào bảng giao diện
            tblHoaDon.setModel(model);
            
            // Chỉnh font cho đẹp (Optional)
            tblHoaDon.getTableHeader().setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 14));
            tblHoaDon.setRowHeight(25);
            
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tải hóa đơn: " + e.getMessage());
        }
    }
