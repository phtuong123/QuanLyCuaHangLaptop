private void loadData() {
        try {
            Connection conn = DBConnect.getConnection();
            String sql = "SELECT * FROM KhachHang ORDER BY MaKH DESC"; // Khách mới nhất lên đầu
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            model.setRowCount(0); // Xóa dữ liệu cũ

            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getInt("MaKH"));
                row.add(rs.getString("HoTen"));
                row.add(rs.getString("SDT"));
                row.add(rs.getString("Email"));
                row.add(rs.getString("DiaChi"));
                model.addRow(row);
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tải dữ liệu: " + e.getMessage());
        }
    }
    
    /**
     * HÀM KIỂM TRA ĐẦU VÀO (Validation)
     */
    private boolean checkValidate() {
        if (txtHoTen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Họ tên khách hàng!");
            txtHoTen.requestFocus();
            return false;
        }
        if (txtSDT.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Số điện thoại!");
            txtSDT.requestFocus();
            return false;
        }
        // Kiểm tra SĐT có phải là số không (cơ bản)
        try {
             Long.parseLong(txtSDT.getText().trim());
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ (chỉ chứa số)!");
             txtSDT.requestFocus();
             return false;
        }
        
        // Email và Địa chỉ có thể để trống tùy nghiệp vụ, ở đây mình cho phép trống.
        return true;
    }
