    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String tuKhoa = txtTimKiem.getText().trim();
        if (tuKhoa.isEmpty()) {
            loadData(); // Nếu rỗng thì load lại hết
            return;
        }
        
        try {
            Connection conn = DBConnect.getConnection();
            // Tìm gần đúng theo tên HOẶC theo số điện thoại
            String sql = "SELECT * FROM KhachHang WHERE HoTen LIKE ? OR SDT LIKE ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            String key = "%" + tuKhoa + "%";
            ps.setString(1, key);
            ps.setString(2, key);
            
            ResultSet rs = ps.executeQuery();
            model.setRowCount(0); // Xóa bảng cũ
            
            while(rs.next()){
                Vector row = new Vector();
                row.add(rs.getInt("MaKH"));
                row.add(rs.getString("HoTen"));
                row.add(rs.getString("SDT"));
                row.add(rs.getString("Email"));
                row.add(rs.getString("DiaChi"));
                model.addRow(row);
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
             JOptionPane.showMessageDialog(this, "Lỗi tìm kiếm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

