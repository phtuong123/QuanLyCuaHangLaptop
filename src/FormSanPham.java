    private void loadComboBoxLoaiSP() {
        try {
            Connection conn = DBConnect.getConnection();
            String sql = "SELECT TenLoai FROM LoaiSP";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            // Khởi tạo model cho ComboBox
            modelComboBox = new DefaultComboBoxModel<>();
            
            // Thêm dòng đầu tiên là "Tất cả" hoặc "Chọn loại"
            modelComboBox.addElement("Chọn loại SP");

            // Duyệt result set và thêm tên loại vào model
            while (rs.next()) {
                modelComboBox.addElement(rs.getString("TenLoai"));
            }

            // Gán model vào ComboBox trên giao diện
            cboLoaiSP.setModel(modelComboBox);

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tải ComboBox: " + e.getMessage());
        }
    }
