    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       if (maSPDangChon == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, 
                "Bạn có chắc chắn muốn xóa sản phẩm này không?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = DBConnect.getConnection();
                String sqlDelete = "DELETE FROM SanPham WHERE MaSP = ?";
                PreparedStatement ps = conn.prepareStatement(sqlDelete);
                ps.setInt(1, maSPDangChon);

                int rows = ps.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!");
                    btnLamMoiActionPerformed(evt); // Reset
                }
                conn.close();
            } catch (java.sql.SQLIntegrityConstraintViolationException e) {
                // Bắt lỗi khóa ngoại (nếu sản phẩm đã có trong ChiTietHD)
                JOptionPane.showMessageDialog(this, 
                        "Không thể xóa sản phẩm này vì đã tồn tại trong hóa đơn bán hàng!\n" +
                        "(Gợi ý: Bạn có thể cập nhật số lượng về 0 thay vì xóa)");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi xóa sản phẩm: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

