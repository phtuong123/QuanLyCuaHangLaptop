    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // 1. Kiểm tra xem người dùng đã chọn dòng nào trên bảng chưa
    int row = tblHoaDon.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Vui lòng chọn hóa đơn cần xóa!");
        return;
    }

    // 2. Hỏi xác nhận cho chắc ăn (tránh xóa nhầm)
    int confirm = javax.swing.JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa hóa đơn này? (Không thể khôi phục)");
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try {
            // Lấy Mã HĐ từ dòng đang chọn (Cột 0)
            String maHD = tblHoaDon.getValueAt(row, 0).toString();
            
            Connection conn = DBConnect.getConnection();
            
            // --- BƯỚC QUAN TRỌNG: XÓA CHI TIẾT TRƯỚC ---
            // Phải xóa hàng hóa trong đơn trước thì mới xóa được cái đơn
            String sqlChiTiet = "DELETE FROM ChiTietHD WHERE MaHD = ?";
            PreparedStatement ps1 = conn.prepareStatement(sqlChiTiet);
            ps1.setString(1, maHD);
            ps1.executeUpdate(); // Thực thi xóa chi tiết
            
            // --- BƯỚC TIẾP THEO: XÓA HÓA ĐƠN ---
            String sqlHoaDon = "DELETE FROM HoaDon WHERE MaHD = ?";
            PreparedStatement ps2 = conn.prepareStatement(sqlHoaDon);
            ps2.setString(1, maHD);
            ps2.executeUpdate(); // Thực thi xóa hóa đơn
            
            javax.swing.JOptionPane.showMessageDialog(null, "Xóa thành công!");
            
            // Tải lại bảng để mất dòng vừa xóa
            loadData();
            
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(null, "Lỗi xóa: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnXoaActionPerformed

