    private void loadData() {
        try {
            Connection conn = DBConnect.getConnection();
            
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Lỗi kết nối CSDL!");
                return;
            }

            // 1. Câu lệnh SQL lấy toàn bộ nhân viên
            String sql = "SELECT * FROM NhanVien";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            // 2. Tạo mô hình bảng (Cấu trúc cột)
            DefaultTableModel model = new DefaultTableModel();
            // Định nghĩa các cột giống hệt trong Database
            model.addColumn("Mã NV"); 
            model.addColumn("Họ Tên");
            model.addColumn("Chức Vụ");
            model.addColumn("Ngày Sinh");
            model.addColumn("Lương");
            model.addColumn("Tài Khoản");
            model.addColumn("Mật Khẩu");
            
            // 3. Duyệt qua từng dòng dữ liệu lấy được và thêm vào model
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getInt("MaNV"));
                row.add(rs.getString("HoTen"));
                row.add(rs.getString("ChucVu"));
                row.add(rs.getDate("NgaySinh"));
                row.add((long) rs.getDouble("Luong"));
                row.add(rs.getString("TaiKhoan"));
                row.add(rs.getString("MatKhau"));
                
                model.addRow(row); // Thêm dòng vào model
            }
            
            // 4. Gán model vừa tạo vào bảng tblNhanVien
            tblNhanVien.setModel(model);
            
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tải dữ liệu: " + e.getMessage());
        }
    }
