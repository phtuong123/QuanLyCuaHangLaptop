    private void loadDataSanPham() {
        try {
            Connection conn = DBConnect.getConnection();
            
            // Câu lệnh JOIN 2 bảng để lấy Tên Loại thay vì Mã Loại
            String sql = "SELECT SP.MaSP, SP.TenSP, L.TenLoai, SP.GiaBan, SP.SoLuongTon, SP.MoTa " +
                         "FROM SanPham SP " +
                         "LEFT JOIN LoaiSP L ON SP.MaLoai = L.MaLoai " + 
                         "ORDER BY SP.MaSP ASC"; // Sắp xếp sản phẩm mới nhất lên đầu
            
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            // Lấy model của bảng hiện tại và xóa hết dữ liệu cũ
            modelSanPham = (DefaultTableModel) tblSanPham.getModel();
            modelSanPham.setRowCount(0);

            // Định dạng tiền tệ (ví dụ: 15000000 -> 15,000,000)
            DecimalFormat df = new DecimalFormat("###,###");

            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getInt("MaSP"));
                row.add(rs.getString("TenSP"));
                row.add(rs.getString("TenLoai")); // Hiển thị tên loại
                
                // Định dạng giá bán
                double giaBan = rs.getDouble("GiaBan");
                row.add(df.format(giaBan)); 
                
                row.add(rs.getInt("SoLuongTon"));
                row.add(rs.getString("MoTa"));

                modelSanPham.addRow(row);
            }

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tải dữ liệu sản phẩm: " + e.getMessage());
        }
    }
