    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
       // 1. Lấy dòng được chọn
        int selectedRow = tblSanPham.getSelectedRow();
        
        if (selectedRow >= 0) {
            // 2. Lấy dữ liệu từ các cột của dòng đó
            // Cột 0 là Mã SP (ẩn hoặc hiện tùy bạn cấu hình, nhưng nó vẫn ở index 0 trong model)
            maSPDangChon = Integer.parseInt(modelSanPham.getValueAt(selectedRow, 0).toString());
            String tenSP = modelSanPham.getValueAt(selectedRow, 1).toString();
            String tenLoai = modelSanPham.getValueAt(selectedRow, 2).toString();
            // Giá bán cần xử lý bỏ dấu phẩy nếu có trước khi đưa lại vào textfield
            String giaBanStr = modelSanPham.getValueAt(selectedRow, 3).toString().replace(",", "");
            String soLuong = modelSanPham.getValueAt(selectedRow, 4).toString();
            // Mô tả có thể null, cần kiểm tra
            Object moTaObj = modelSanPham.getValueAt(selectedRow, 5);
            String moTa = (moTaObj != null) ? moTaObj.toString() : "";

            // 3. Đổ dữ liệu lên các ô nhập liệu
            txtTenSP.setText(tenSP);
            cboLoaiSP.setSelectedItem(tenLoai); // Tự động chọn loại tương ứng trong combobox
            txtGiaBan.setText(giaBanStr);
            txtSoLuong.setText(soLuong);
        
            
            // (Tùy chọn) Khóa nút Thêm, mở nút Sửa/Xóa khi đang chọn dòng
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tblSanPhamMouseClicked

