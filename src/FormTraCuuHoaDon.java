    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String tuKhoa = txtTimKiem.getText().trim();

        if (tuKhoa.isEmpty()) {
            loadData();
            return;
        }

        try {
            Connection conn = DBConnect.getConnection();

            // --- SQL NÂNG CẤP: TÌM THÊM CẢ NGÀY LẬP ---
            // DATE_FORMAT(H.NgayLap, '%d/%m/%Y') chuyển ngày trong DB thành chuỗi '21/11/2025' để dễ tìm
            String sql = "SELECT H.MaHD, H.NgayLap, N.HoTen AS TenNV, K.HoTen AS TenKH, H.TongTien " +
            "FROM HoaDon H " +
            "JOIN NhanVien N ON H.MaNV = N.MaNV " +
            "JOIN KhachHang K ON H.MaKH = K.MaKH " +
            "WHERE N.HoTen LIKE ? OR K.HoTen LIKE ? OR DATE_FORMAT(H.NgayLap, '%d/%m/%Y') LIKE ?";

            java.sql.PreparedStatement ps = conn.prepareStatement(sql);

            // Set 3 tham số giống nhau (tìm bất kỳ đâu)
            ps.setString(1, "%" + tuKhoa + "%"); // Tìm Tên NV
            ps.setString(2, "%" + tuKhoa + "%"); // Tìm Tên KH
            ps.setString(3, "%" + tuKhoa + "%"); // Tìm Ngày (ví dụ nhập '21' tìm ra ngày 21)

            java.sql.ResultSet rs = ps.executeQuery();

            // --- Đổ dữ liệu ra bảng (Code cũ giữ nguyên) ---
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
            model.addColumn("Mã HĐ");
            model.addColumn("Ngày Lập");
            model.addColumn("Nhân Viên");
            model.addColumn("Khách Hàng");
            model.addColumn("Tổng Tiền");

            while (rs.next()) {
                java.util.Vector row = new java.util.Vector();
                row.add(rs.getInt("MaHD"));

                // Format ngày giờ đẹp
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");
                row.add(sdf.format(rs.getTimestamp("NgayLap")));

                row.add(rs.getString("TenNV"));
                row.add(rs.getString("TenKH"));

                // Format tiền đẹp
                java.text.DecimalFormat df = new java.text.DecimalFormat("###,###");
                row.add(df.format(rs.getDouble("TongTien")));

                model.addRow(row);
            }
            tblHoaDon.setModel(model);

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi tìm kiếm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

