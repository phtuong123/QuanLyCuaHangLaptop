    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        // 1. Lấy từ khóa người dùng nhập và cắt khoảng trắng thừa
    String tuKhoa = txtTimKiem.getText().trim();
    
    // Nếu ô trống -> Tải lại toàn bộ danh sách (Co như nút Làm mới)
    if (tuKhoa.isEmpty()) {
        loadData(); 
        return;
    }

    try {
        Connection conn = DBConnect.getConnection();
        
        // --- CÂU LỆNH SQL THÔNG MINH ---
        // Tìm kiếm: Hoặc Tên chứa từ khóa, Hoặc Mã NV chứa từ khóa
        String sql = "SELECT * FROM NhanVien WHERE HoTen LIKE ? OR MaNV LIKE ?";
        
        PreparedStatement ps = conn.prepareStatement(sql);
        
        // Dấu % ở hai đầu nghĩa là tìm "có chứa" (Ví dụ nhập "1" thì tìm ra 1, 10, 12, 21...)
        ps.setString(1, "%" + tuKhoa + "%"); // Tìm theo Tên
        ps.setString(2, "%" + tuKhoa + "%"); // Tìm theo Mã
        
        ResultSet rs = ps.executeQuery();
        
        // --- ĐOẠN NÀY COPY Y CHANG HÀM LOAD DATA ---
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã NV"); 
        model.addColumn("Họ Tên");
        model.addColumn("Chức Vụ");
        model.addColumn("Ngày Sinh");
        model.addColumn("Lương");
        model.addColumn("Tài Khoản");
        model.addColumn("Mật Khẩu");
        
        while (rs.next()) {
            Vector row = new Vector();
            row.add(rs.getInt("MaNV"));
            row.add(rs.getString("HoTen"));
            row.add(rs.getString("ChucVu"));
            row.add(rs.getDate("NgaySinh"));
            row.add((long)rs.getDouble("Luong")); 
            row.add(rs.getString("TaiKhoan"));
            row.add(rs.getString("MatKhau"));
            model.addRow(row);
        }
        // Cập nhật bảng với dữ liệu tìm được
        tblNhanVien.setModel(model);
        // -----------------------------------------------
        
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi tìm kiếm: " + e.getMessage());
    }
    }//GEN-LAST:event_btnTimKiemActionPerformed

